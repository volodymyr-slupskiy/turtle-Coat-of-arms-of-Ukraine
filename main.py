import turtle
from turtle import *
t = Pen()
setup(800, 700)

title('ГЕРБ УКРАЇНИ')

#background
t.up()
t.goto(-400, 0)
t.speed(5)
t.color('blue')
t.down()
t.begin_fill()
t.goto(400, 0)
t.goto(400, 350)
t.goto(-400, 350)
t.goto(-400, 0)
t.end_fill()
t.color('yellow')
t.begin_fill()
t.goto(400, 0)
t.goto(400, -350)
t.goto(-400, -350)
t.goto(-400, 0)
t.end_fill()
#background Coat of arms
t.up()
t.goto(-200, 250)
t.speed(5)
t.down()
t.width(10)
t.color('gold', 'blue')
t.begin_fill()
t.goto(-200, -150)
t.goto(0, -250)
t.goto(200, -150)
t.goto(200, 250)
t.goto(-200, 250)
t.end_fill()
#create Coat of arms
t.up()
t.color('yellow')
t.goto(0, 200)
t.width(10)
t.speed(5)
t.down()
t.goto(-15, 130)
t.goto(0, -25)
t.goto(-65, -100)
t.goto(-130, 0)
t.goto(-85, 90)
t.goto(-130, 150)
t.goto(-130, -100)
t.goto(-65, -100)
t.goto(0, -200)
t.goto(65, -100)
t.goto(130, -100)
t.goto(130, 150)
t.goto(85, 90)
t.goto(130, 0)
t.goto(65, -100)
t.goto(0, -25)
t.goto(15, 130)
t.goto(0, 200)
#add text from hymn
t.ht()
